<script>
document.addEventListener("DOMContentLoaded", () => {
  const counters = document.querySelectorAll(".stat");

  const animateValue = (el, start, end, duration, suffix = "") => {
    let startTime = null;

    const step = (timestamp) => {
      if (!startTime) startTime = timestamp;
      const progress = Math.min((timestamp - startTime) / duration, 1);
      const value = (progress * (end - start) + start).toFixed(end % 1 === 0 ? 0 : 1);

      el.querySelector(".num").textContent = value;
      if (progress < 1) requestAnimationFrame(step);
      else el.querySelector(".num").textContent = end;
    };

    requestAnimationFrame(step);
  };

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        const counter = entry.target;
        if (counter.classList.contains("counted")) return;

        const target = parseFloat(counter.dataset.target);
        const manualSuffix = counter.dataset.suffix;

        // 🔧 logika baru
        let suffix;
        if (manualSuffix !== undefined) {
          suffix = manualSuffix.trim() === "" ? "" : manualSuffix;
        } else if (target >= 100) {
          suffix = "MILLION+";
        } else {
          suffix = "M+";
        }

        // Inisialisasi isi elemen
        counter.innerHTML = `
          <span class="num">0</span>
          <span class="suffix">${suffix}</span>
        `;

        animateValue(counter, 0, target, 2000, suffix);
        counter.classList.add("counted");
      }
    });
  }, { threshold: 0.3 });

  counters.forEach((el) => observer.observe(el));
});

</script>

